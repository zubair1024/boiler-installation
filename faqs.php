	<h2>Understanding Your Boiler</h2>
	<div id="accordionTwo">
		<div class="card">
		  <div class="card-header" id="headingTwoOne">
		    <h5 class="mb-0">
		      <button class="btn btn-link collapsed" data-toggle="collapse" data-target="#collapseTwoOne" aria-expanded="false" aria-controls="collapseTwoOne">
		        What is a system boiler?
		      </button>
		    </h5>
		  </div>
		  <div id="collapseTwoOne" class="collapse" aria-labelledby="headingTwoOne" data-parent="#accordionTwo">
		    <div class="card-body">
		      <p>System boilers heat your radiators directly and they usually store hot water in a storage cylinder. These are commonly located in either an airing cupboard or in a garage. They heat a large volume of water at once and then store this hot water until you decide to use it. They are less efficient than combi boilers, which only heat the water you need, when you need it.</p>
		    </div>
		  </div>
		</div>
		<div class="card">
		  <div class="card-header" id="headingTwoTwo">
		    <h5 class="mb-0">
		      <button class="btn btn-link collapsed" data-toggle="collapse" data-target="#collapseTwoTwo" aria-expanded="false" aria-controls="collapseTwoTwo">
		        What is a standard boiler?
		      </button>
		    </h5>
		  </div>
		  <div id="collapseTwoTwo" class="collapse" aria-labelledby="headingTwoTwo" data-parent="#accordionTwo">
		    <div class="card-body">
		      <p>Also known as conventional boiler, the standard boiler stores hot water in a storage cylinder, similar to system boilers, but the difference is that a standard boiler is fed by a separate coldwater tank – commonly found in the loft. There's another separate tank, called a ‘feed and expansion cistern', usually in the loft too. Standard boilers are the largest type of boiler and so are not generally suitable for smaller living spaces.</p>
		    </div>
		  </div>
		</div>
